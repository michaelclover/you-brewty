def kg_to_lb(kg):
    return kg * 2.204623

def lb_to_kg(lb):
    return lb / 2.204623

def us_gallon_to_litre(gal):
    return gal * 3.785411784

def litre_to_us_gallon(litre):
    return litre / 3.785411784

def to_ppm(g):
    return (g / 1000) + 1

def pts_to_pct(pts):
    return 1.0 + (pts / 1000)

def ounces_to_grams(weight):
    return weight * 28.35

def grams_to_ounces(weight):
    return weight / 28.35

def us_gallon_to_quart(gal):
    return gal * 4